export const PLAYLIST_THEMES = [
  { name: "Work", data: WorkingBackground },
  { name: "Romactic", data: RomanticBackground },
  { name: "Relax", data: RelaxBackground },
  { name: "Sadness", data: SadnessBackground },
  { name: "Sleep", data: SleepBackground },
  { name: "Playlist", data: PlaylistBackground },
];
export const getRandomBackground = (playlistName: string): Video | null => {
  const playlist = PLAYLIST_THEMES.find(
    (theme) => theme.name.toLowerCase() === playlistName.toLowerCase()
  );

  if (!playlist || !playlist.data.length) {
    return null;
  }

  const randomIndex = Math.floor(Math.random() * playlist.data.length);
  return playlist.data[randomIndex];
};
